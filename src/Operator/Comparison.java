package Operator;

public class Comparison {
        public static void main(String[] args) {
            int a = 10, b = 5;
            System.out.println("a == b: " + (a == b));    // Output: false
            System.out.println("a != b: " + (a != b));    // Output: true
            System.out.println("a > b: " + (a > b));      // Output: true
            System.out.println("a < b: " + (a < b));      // Output: false
        }
    }
