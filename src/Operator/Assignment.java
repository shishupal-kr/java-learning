package Operator;

public class Assignment {
        public static void main(String[] args) {
            int a = 10;
            a += 5;  // a = a + 5
            System.out.println("a += 5: " + a);  // Output: 15
            a *= 2;  // a = a * 2
            System.out.println("a *= 2: " + a);  // Output: 30
        }
    }