package Operator;

public class Logical {
        public static void main(String[] args) {
            boolean a = true, b = false;
            System.out.println("a && b: " + (a && b));  // Output: false
            System.out.println("a || b: " + (a || b));  // Output: true
            System.out.println("!a: " + (!a));          // Output: false
        }
    }
